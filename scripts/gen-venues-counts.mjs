#!/usr/bin/env node
// venues.ts에서 open venue 카운트 + TOP4 정적 추출 → src/data/venues-counts.ts + venues-top4.ts.
// vite build 전에 실행 (package.json prebuild). HomePage가 venues 전체를 dynamic import하므로
// 정적 카운트/TOP4가 LCP/위폴드에 필요.
import { readFileSync, writeFileSync } from 'node:fs';

const src = readFileSync('src/data/venues.ts', 'utf8');
const blocks = src.split(/(?=^\s+id:\s*'v-\d+')/m).slice(1);
const cats = { club: 0, night: 0, lounge: 0, room: 0, yojeong: 0, hoppa: 0 };
let open = 0;
const top4 = [];

function pick(b, key) {
  const re = new RegExp(`${key}:\\s*'([^']*)'`);
  const m = b.match(re);
  return m ? m[1] : '';
}
function pickNum(b, key) {
  const re = new RegExp(`${key}:\\s*([0-9.]+)`);
  const m = b.match(re);
  return m ? Number(m[1]) : 0;
}

for (const b of blocks) {
  if (/status:\s*'closed_or_unclear'/.test(b)) continue;
  open++;
  const cat = pick(b, 'category');
  if (cats[cat] !== undefined) cats[cat]++;
  if (top4.length < 4) {
    top4.push({
      id: pick(b, 'id'),
      slug: pick(b, 'slug'),
      nameKo: pick(b, 'nameKo'),
      regionKo: pick(b, 'regionKo'),
      region: pick(b, 'region'),
      category: cat,
      rating: pickNum(b, 'rating'),
    });
  }
}

const countsOut = `// AUTO-GENERATED by scripts/gen-venues-counts.mjs — do not edit
// venues 전체 데이터(341KB) 분리. 카운트 등 정적 숫자만 필요한 곳에서 이걸 import.
// 이 모듈은 import해도 chunk 가벼움 → / 페이지 LCP 영향 0.
export const VENUES_TOTAL_OPEN = ${open};
export const VENUES_BY_CATEGORY: Record<string, number> = ${JSON.stringify(cats, null, 2)};
`;
writeFileSync('src/data/venues-counts.ts', countsOut);

const top4Out = `// AUTO-GENERATED by scripts/gen-venues-counts.mjs — do not edit
// HomePage 위폴드 "지금 핫한 곳" 4카드 정적 임베드. venues.ts(341KB) 로드 전에 즉시 렌더 → LCP/TBT 단축.
// VenueCard가 사용하는 필드만 추출 (id/slug/nameKo/regionKo/region/category/rating).
export type VenueTop4 = {
  id: string;
  slug: string;
  nameKo: string;
  regionKo: string;
  region: string;
  category: string;
  rating: number;
};
export const VENUES_TOP4: VenueTop4[] = ${JSON.stringify(top4, null, 2)};
`;
writeFileSync('src/data/venues-top4.ts', top4Out);

console.log(`✅ venues-counts.ts 갱신 (total ${open}, ${JSON.stringify(cats)})`);
console.log(`✅ venues-top4.ts 갱신 (${top4.map(v => v.nameKo).join(', ')})`);
