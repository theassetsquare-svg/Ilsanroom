// 동네 라벨에 광역시명이 접두로 붙은 경우(예 "부산 해운대") locality는 동네만 남긴다.
export function localityOf(regionKo) {
  return (regionKo || '').replace(/^(서울|부산|대구|대전|광주|울산|인천)\s+/, '') || regionKo;
}

// 행정구역(시/도) 조회. 미매핑이면 null → 게이트가 빌드 차단.
export function provinceOf(regionKo) {
  return REGION_PROVINCE[regionKo] || null;
}
