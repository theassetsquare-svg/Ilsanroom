// Track usage index per replacement pool
const usageIdx = {};
for (const key of Object.keys(replacements)) usageIdx[key] = 0;

function getUnique(key) {
  const pool = replacements[key];
  if (!pool) return null;
  const idx = usageIdx[key] % pool.length;
  usageIdx[key]++;
  return pool[idx];
}

// Also remove/replace these generic filler sentences
