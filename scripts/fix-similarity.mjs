const fillerReplacements = [
  '', // just remove - descriptions are long enough
];

let changeCount = 0;

// Apply replacements
for (const [pattern, pool] of Object.entries(replacements)) {
  // Find all occurrences and replace each with a unique alternative
  let idx = 0;
  while (content.includes(pattern)) {
    const replacement = pool[idx % pool.length];
    content = content.replace(pattern, replacement);
    idx++;
    changeCount++;
  }
}

// Remove generic fillers
for (const filler of genericFillers) {
  while (content.includes(filler)) {
    content = content.replace(filler, '. ');
    changeCount++;
  }
}

fs.writeFileSync(filePath, content);
console.log(`✅ ${changeCount}개 반복 문구 교체/제거 완료`);
