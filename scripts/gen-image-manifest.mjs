#!/usr/bin/env node
/**
 * public/venues/{slug}-1.{webp,jpg,png}를 스캔해 실제 이미지 보유 slug Set을
 * src/data/venue-image-manifest.ts로 emit. 빌드 직전 실행.
 * 누락 venue는 컴포넌트가 img 태그 자체를 skip → SPA fallback 7KB HTML 응답 절약.
 */
import fs from 'fs';
import path from 'path';

const VENUES_DIR = path.resolve('public/venues');
const OUT = path.resolve('src/data/venue-image-manifest.ts');

const files = fs.readdirSync(VENUES_DIR);
const slugs = new Set();
const slotsBySlug = new Map(); // slug → Set<number> (1~4)
for (const f of files) {
  const m = f.match(/^(.+?)-(\d+)\.(webp|jpg|png)$/);
  if (m) {
    const slug = m[1];
    const n = Number(m[2]);
    if (n === 1) slugs.add(slug);
    if (n >= 1 && n <= 4) {
      if (!slotsBySlug.has(slug)) slotsBySlug.set(slug, new Set());
      slotsBySlug.get(slug).add(n);
    }
  }
}

// 슬러그별 광고 override는 항상 유효 슬롯 (이미지 파일 없어도 컴포넌트가 별도 경로 사용)
const OVERRIDE_SLOTS = { 'haeundaehoppa-kkantappiya': [4] };
for (const [slug, ns] of Object.entries(OVERRIDE_SLOTS)) {
  if (!slotsBySlug.has(slug)) slotsBySlug.set(slug, new Set());
  for (const n of ns) slotsBySlug.get(slug).add(n);
}

const sorted = [...slugs].sort();
const slotEntries = [...slotsBySlug.entries()]
  .filter(([s]) => slugs.has(s) || OVERRIDE_SLOTS[s])
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([s, ns]) => `  ['${s}', [${[...ns].sort((a, b) => a - b).join(', ')}]]`);

const ts = `// AUTO-GENERATED by scripts/gen-image-manifest.mjs — do not edit.
// public/venues/{slug}-1.{webp,jpg,png} 보유 slug 집합.
export const VENUE_IMAGE_SLUGS: ReadonlySet<string> = new Set([
${sorted.map(s => `  '${s}',`).join('\n')}
]);

export const hasVenueImage = (slug: string): boolean => VENUE_IMAGE_SLUGS.has(slug);

// 시즌29 — 슬러그별 실제 보유 이미지 슬롯 (1~4). 누락 슬롯은 컴포넌트가 렌더 skip → 404 0건.
const VENUE_IMAGE_SLOTS = new Map<string, readonly number[]>([
${slotEntries.join(',\n')}
]);

export function getVenueImageSlots(slug: string): readonly number[] {
  return VENUE_IMAGE_SLOTS.get(slug) ?? [];
}
`;
fs.writeFileSync(OUT, ts);
const totalSlots = [...slotsBySlug.values()].reduce((a, b) => a + b.size, 0);
console.log(`✓ venue-image-manifest: ${sorted.length} slugs, ${totalSlots} slots → ${path.relative(process.cwd(), OUT)}`);
