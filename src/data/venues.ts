// ── Helper Functions ──────────────────────────────────────

export function getVenuesByCategory(category: VenueCategory): Venue[] {
  return venues.filter((v) => v.category === category && v.status !== 'closed_or_unclear');
}

export function getVenueBySlug(slug: string): Venue | undefined {
  return venues.find((v) => v.slug === slug);
}

export function getVenuesByRegion(region: string): Venue[] {
  return venues.filter(
    (v) =>
      (v.region.toLowerCase() === region.toLowerCase() || v.regionKo === region) &&
      v.status !== 'closed_or_unclear',
  );
}

export function getPremiumVenues(): Venue[] {
  return venues.filter((v) => v.isPremium && v.status !== 'closed_or_unclear');
}

export function getVenuesByCategoryAndRegion(category: VenueCategory, region: string): Venue[] {
  return venues.filter(
    (v) =>
      v.category === category &&
      (v.region.toLowerCase() === region.toLowerCase() || v.regionKo === region) &&
      v.status !== 'closed_or_unclear',
  );
}

export function getPopularVenues(limit: number = 8): Venue[] {
  return [...venues]
    .filter((v) => v.status !== 'closed_or_unclear')
    .sort((a, b) => b.reviewCount - a.reviewCount)
    .slice(0, limit);
}

export function getCategoryByKey(key: string): CategoryInfo | undefined {
  return categories.find((c) => c.key === key);
}

export function getRelatedVenues(venue: Venue, limit: number = 4): Venue[] {
  return venues
    .filter(
      (v) =>
        v.id !== venue.id &&
        (v.category === venue.category || v.region === venue.region) &&
        v.status !== 'closed_or_unclear',
    )
    .slice(0, limit);
}
