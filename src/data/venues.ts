export function getPopularVenues(count = 10): Venue[] {
  return venues.filter(v => v.status !== 'closed_or_unclear').slice(0, count);
}

export function getVenueBySlug(slug: string): Venue | undefined {
  return venues.find(v => v.slug === slug);
}

export function getVenuesByCategory(category: VenueCategory): Venue[] {
  return venues.filter(v => v.category === category && v.status !== 'closed_or_unclear');
}

export function getVenuesByCategoryAndRegion(category: VenueCategory, region: string): Venue[] {
  return venues.filter(v => v.category === category && v.region === region && v.status !== 'closed_or_unclear');
}

export function getRelatedVenues(venue: Venue, count = 6): Venue[] {
  return venues
    .filter(v => v.id !== venue.id && v.status !== 'closed_or_unclear' && (v.category === venue.category || v.region === venue.region))
    .slice(0, count);
}

